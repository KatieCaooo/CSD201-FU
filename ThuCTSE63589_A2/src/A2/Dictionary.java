    private void btTranslateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTranslateActionPerformed
        // TODO add your handling code here:
        String searchWord = txtSearchWord.getText();

        if (searchWord.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Word to translate cannot be empty!");
            return;
        }

        try {
            String words;
            int index = cbLanguage.getSelectedIndex();
            if (index == 0) {
                words = englishTree.search(searchWord);
            } else {
                words = vietnameseTree.search(searchWord);
            }
            txtAreaResult.setText(words);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btTranslateActionPerformed

    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // TODO add your handling code here:
        String englishWord = txtEnglishWord.getText();
        String vietnameseWord = txtVietnameseWord.getText();

        if (englishWord.isEmpty() || vietnameseWord.isEmpty()) {
            JOptionPane.showMessageDialog(null, "A couple word cannot be empty!");
            return;
        }

        try {
            englishTree.add(englishWord, vietnameseWord);
            vietnameseTree.add(vietnameseWord, englishWord);
            JOptionPane.showMessageDialog(null, "A couple word was added to dictionary!");
            txtEnglishWord.setText("");
            txtVietnameseWord.setText("");
            flagAdd = true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btAddActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        // TODO add your handling code here:
        int exit = JOptionPane.showConfirmDialog(this, "Do you want to exit?", "Exit", JOptionPane.YES_NO_OPTION);
        if (exit == 0) {
            int choose = JOptionPane.showConfirmDialog(this, "Do you want to save file?", "Save to file..",
                    JOptionPane.YES_NO_CANCEL_OPTION);
            if (choose == 0) {
                if (flagAdd) {
                    saveData("EN-VN.txt", englishTree);
                    saveData("VN-EN.txt", vietnameseTree);
                    JOptionPane.showMessageDialog(this, "Saved to file!");
                }
                setDefaultCloseOperation(Dictionary.EXIT_ON_CLOSE);
            }
            if (choose == 1) {
                setDefaultCloseOperation(Dictionary.EXIT_ON_CLOSE);
            }
            if (choose == 2) {
                setDefaultCloseOperation(Dictionary.DO_NOTHING_ON_CLOSE);
            }
        }
        if (exit == 1) {
            setDefaultCloseOperation(Dictionary.DO_NOTHING_ON_CLOSE);
        }
    }//GEN-LAST:event_formWindowClosing

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (flagAdd) {
            saveData("EN-VN.txt", englishTree);
            saveData("VN-EN.txt", vietnameseTree);
            JOptionPane.showMessageDialog(this, "Saved to file!");
            flagAdd = false;
        } else {
            JOptionPane.showMessageDialog(this, "Please add new word to your dictionary first!");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

