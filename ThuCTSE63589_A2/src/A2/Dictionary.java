    private void loadData(String file, BinaryTree tree) {
        FileReader fr = null;
        BufferedReader br = null;

        try {
            fr = new FileReader(file);
            br = new BufferedReader(fr);

            String line, key;
            String[] values;
            while ((line = br.readLine()) != null) {
                key = line.split(":")[0];
                values = line.split(":")[1].split(",");

                for (int i = 0; i < values.length; i++) {
                    tree.add(key, values[i]);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } finally {
            try {
                if (br != null) {
                    br.close();
                }
                if (fr != null) {
                    fr.close();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }

    private void saveData(String file, BinaryTree tree) {
        try {
            Stack<BinaryTree> stack = new Stack();
            PrintWriter pw = new PrintWriter(file);
            BinaryTree node = tree;

            while (node != null) {
                stack.push(node);
                node = node.left;
            }

            while (!stack.empty()) {
                node = stack.pop();
                pw.println(node.word + ":" + node.linkedListWord.getWords().replaceAll(", ", ","));

                if (node.right != null) {
                    node = node.right;

                    while (node != null) {
                        stack.push(node);
                        node = node.left;
                    }
                }
            }
            pw.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

